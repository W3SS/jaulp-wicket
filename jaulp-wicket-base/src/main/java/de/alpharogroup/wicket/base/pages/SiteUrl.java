package de.alpharogroup.wicket.base.pages;

/**
 * The Class SiteUrl.
 */
public class SiteUrl
{

	/** The loc. */
	private final String loc;

	/**
	 * Instantiates a new site url.
	 *
	 * @param loc
	 *            the loc
	 */
	public SiteUrl(final String loc)
	{
		this.loc = loc;
	}

	/**
	 * Gets the loc.
	 *
	 * @return the loc
	 */
	public String getLoc()
	{
		return this.loc;
	}
}